package com.Agendamento.demo.Entities;

public class Users {
    private String name;
    private String email;

    public Users(String name, String email){
        this.name = name;
        this.email = email;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
